    private void limparTela(){
        jTextFieldIdentificador.setText("");
        jTextFieldNomeDaEscola.setText("");
        jTextFieldTelefone.setText("");
        jTextFieldEmail.setText("");
        jTextFieldEndereco.setText("");
    }
    
    private void mostrarDadosDasEscolas(){
        try{
            List<Escola> listaDasEscolas = escolaControle.listagem();
            DefaultTableModel model = (DefaultTableModel) jTableEscolas.getModel();
            model.setNumRows(0);
            
            Iterator<Escola> it = listaDasEscolas.iterator();
            while(it.hasNext()){
                String[] saida = new String[5];
                Escola aux = it.next();
                saida[0] = String.valueOf(aux.getId());
                saida[1] = aux.getNome();
                saida[2] = aux.getTelefone();
                saida[3] = aux.getEmail();
                saida[4] = aux.getEndereco();
                
                model.addRow(saida);
            }
            
        } catch(Exception erro){
            JOptionPane.showMessageDialog(this, erro.getMessage());
        }
    }
    
    private String[] lerAsEntradas(){
        String[] entradas = new String[5];
        
        entradas[ID] = jTextFieldIdentificador.getText();
        entradas[NOME] = jTextFieldNomeDaEscola.getText();
        entradas[TELEFONE] = jTextFieldTelefone.getText();
        entradas[EMAIL] = jTextFieldEmail.getText();
        entradas[ENDERECO] = jTextFieldEndereco.getText();
        
        return entradas;
    }
    
    private Escola alterarObjEscola(Escola objEscola, String[] entradas){
        if(!entradas[NOME].isBlank()){
            objEscola.setNome(entradas[NOME]);
        }
            
        if(!entradas[TELEFONE].isBlank()){
            objEscola.setTelefone(entradas[TELEFONE]);
        }
            
        if(!entradas[EMAIL].isBlank()){
            objEscola.setEmail(entradas[EMAIL]);
        }
            
        if(!entradas[ENDERECO].isBlank()){
            objEscola.setEndereco(entradas[ENDERECO]);
        }
            
        return objEscola;    
    }
    
    private Escola criarObjEscola(String[] entradas) throws Exception{
        try{       
            Escola objEscola = new Escola(0, 
                                entradas[NOME], entradas[TELEFONE], 
                                entradas[EMAIL], entradas[ENDERECO]);
            
            return objEscola;
            
        } catch(Exception erro) {
            throw erro;
        }
    }

