    private void jButtonIncluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonIncluirActionPerformed
         // TODO add your handling code here:
         try {
            String[] entradas = lerAsEntradas();
            Escola objEscola = criarObjEscola(entradas);
            escolaControle.incluir(objEscola);
            limparTela();
            mostrarDadosDasEscolas();
            
         } catch (Exception erro) {
             JOptionPane.showMessageDialog(this, erro.getMessage());
         }
    }//GEN-LAST:event_jButtonIncluirActionPerformed

    private void jButtonAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAlterarActionPerformed
        try {
            String[] entradas = lerAsEntradas();
            int id = Integer.parseInt(entradas[ID]);
            Escola objEscola = escolaControle.consultarPorID(id);
            objEscola = alterarObjEscola(objEscola, entradas);
            escolaControle.alterar(objEscola);
            limparTela();
            mostrarDadosDasEscolas();
            JOptionPane.showMessageDialog(this, "Alterado com sucesso.");
            
         } catch (Exception erro) {
             JOptionPane.showMessageDialog(this, erro.getMessage());
         }
    }//GEN-LAST:event_jButtonAlterarActionPerformed

